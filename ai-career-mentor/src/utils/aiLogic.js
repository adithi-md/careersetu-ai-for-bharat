const generateExplanation = (scores, formData, skillGaps) => {
  const weakSkills = skillGaps.filter(s => s.gap > 10).map(s => s.name).slice(0, 3).join(", ");
  const departmentText = formData.department;

  if (scores.gap > 20) {
    return `This readiness score is generated by analyzing the candidate's skills in ${departmentText} and comparing them with industry hiring requirements for ${departmentText.toLowerCase()} roles. Significant preparation needed. Focus on ${weakSkills || "core fundamentals"}. Your current readiness is ${scores.current}% but ${formData.targetCompany} expects ${scores.target}%.`;
  } else if (scores.gap > 10) {
    return `This readiness score is generated by analyzing the candidate's skills in ${departmentText} and comparing them with industry hiring requirements for ${departmentText.toLowerCase()} roles. You're on the right track but need focused improvement in ${weakSkills || "key areas"}. Bridge the ${scores.gap}% gap with consistent practice.`;
  } else {
    return `This readiness score is generated by analyzing the candidate's skills in ${departmentText} and comparing them with industry hiring requirements for ${departmentText.toLowerCase()} roles. Strong profile! You're ${scores.current}% ready. Fine-tune your skills and you'll be competitive for ${formData.targetCompany}.`;
  }
};

