export const generateAIAnalysis = (formData) => {
  const scores = calculateReadiness(formData, formData.department);
  const candidateSkills = generateSkillScores(formData.department, scores.current);
  const companyRequirements = getCompanyRequirements(formData.targetCompany, formData.department);
  const skillGaps = calculateSkillGaps(candidateSkills, companyRequirements);
  
  return {
    scores,
    company: getCompanyData(formData.targetCompany),
    candidateSkills,
    companyRequirements,
    skillGaps,
    aiExplanation: generateExplanation(scores, formData, skillGaps),
    roadmap: generateRoadmap(formData, skillGaps),
    studyTopics: getStudyTopics(formData.department)
  };
};
